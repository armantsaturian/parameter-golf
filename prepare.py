"""
prepare.py — Read-only evaluation harness for the parameter-golf autoresearch loop.

This file is imported by train.py:  ``from prepare import ...``

It contains the trusted, unmodifiable evaluation code, data-loading utilities,
tokenizer BPB lookup-table builder, and artifact-verification logic.  The AI
agent is NOT allowed to edit this file; only the human operator may change it.

Key contracts
-------------
* ``eval_val``            — standard BPB evaluation (non-overlapping windows).
* ``eval_val_sliding``    — sliding-window BPB evaluation (used by top entries).
                           The model must expose a ``forward_logits(input_ids) -> logits``
                           method (returns raw logits, no loss computation).
* ``verify_artifact``     — checks compressed-model + code ≤ MAX_ARTIFACT_BYTES.
* When run as ``python prepare.py`` it downloads FineWeb data via the helper script.
"""

