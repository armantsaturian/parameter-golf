# ARTIFACT VERIFICATION
# ---------------------------------------------------------------------------

def verify_artifact(
    artifact_path: str | Path,
    code_path: str | Path,
) -> tuple[int, int, int, bool]:
    """Check that compressed model + code fit within the size budget.

    Returns (artifact_bytes, code_bytes, total_bytes, is_valid).
    """
    artifact_path = Path(artifact_path)
    code_path = Path(code_path)

    if not artifact_path.exists():
        raise FileNotFoundError(f"Artifact not found: {artifact_path}")
    if not code_path.exists():
        raise FileNotFoundError(f"Code file not found: {code_path}")

    artifact_bytes = artifact_path.stat().st_size
    code_bytes = code_path.stat().st_size
    total_bytes = artifact_bytes + code_bytes
    is_valid = total_bytes <= MAX_ARTIFACT_BYTES

    return artifact_bytes, code_bytes, total_bytes, is_valid


